fruits= ["apple", "banana", "cherry"]
fruits.append("orange")  # adds orange to the end of the list
print(fruits)  # prints the updated list with orange added

fruits.insert(1, "kiwi")  # adds kiwi at index 1
print(fruits)  # prints the updated list with kiwi added at index 1

fruits.remove("banana")  # removes banana from the list
print(fruits)  # prints the updated list with banana removed

fruits.sort()  # sorts the list in alphabetical order
print(fruits)  # prints the sorted list